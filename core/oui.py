_in_memory_db: Optional[dict[str, str]] = None
_db_lock = threading.Lock()


def _normalize_mac(mac: str) -> str:
    """Normalize MAC to lowercase colon-separated OUI prefix (6 hex bytes)."""
    clean = re.sub(r'[^a-fA-F0-9]', '', mac).lower()
    if len(clean) < 6:
        return ""
    prefix = ":".join(clean[i:i+2] for i in range(0, 6, 2))
    return prefix


def _get_cache_path() -> Path:
    """Get path to cached OUI database."""
    appdata = Path(os.environ.get("LOCALAPPDATA", Path.home() / "AppData" / "Local"))
    cache_dir = appdata / "ZeeCut"
    cache_dir.mkdir(parents=True, exist_ok=True)
    return cache_dir / OUI_CACHE_FILE


def download_oui_database() -> bool:
    """Download full OUI database from IEEE and cache locally."""
    try:
        ctx = ssl.create_default_context()
        req = urllib.request.Request(OUI_URL, headers={
            "User-Agent": "ZeeCut/1.0 OUI Lookup"
        })
        with urllib.request.urlopen(req, timeout=30, context=ctx) as resp:
            data = resp.read().decode("utf-8", errors="replace")

        db: dict[str, str] = {}
        lines = data.splitlines()
        for line in lines[1:]:
            parts = line.strip().strip('"').split('","')
            if len(parts) >= 3:
                oui_hex = parts[1].strip().replace("-", ":").lower()
                vendor = parts[2].strip()
                if oui_hex and vendor:
                    # Store first 6 hex chars (OUI)
                    clean_oui = ":".join(oui_hex[i:i+2] for i in range(0, 6, 2))
                    if clean_oui and len(clean_oui) == 8:
                        db[clean_oui] = vendor

        cache_path = _get_cache_path()
        cache_path.write_text(json.dumps(db, indent=0, separators=(",", ":")), encoding="utf-8")

        with _db_lock:
            global _in_memory_db
            _in_memory_db = db

        logger.info("Downloaded OUI database: %d entries", len(db))
        return True
    except Exception as e:
        logger.warning("Failed to download OUI database: %s", e)
        return False


def _load_oui_db() -> dict[str, str]:
    """Load OUI database into memory with caching."""
    global _in_memory_db
    if _in_memory_db is not None:
        return _in_memory_db

    with _db_lock:
        if _in_memory_db is not None:
            return _in_memory_db

        cache_path = _get_cache_path()
        if cache_path.exists():
            try:
                data = json.loads(cache_path.read_text(encoding="utf-8"))
                if isinstance(data, dict):
                    _in_memory_db = data
                    logger.info("Loaded OUI database from cache: %d entries", len(data))
                    return _in_memory_db
            except Exception as e:
                logger.warning("Failed to load OUI cache: %s", e)

        _in_memory_db = dict(COMMON_VENDORS)
        return _in_memory_db


def lookup_vendor(mac: str) -> str:
    """Look up vendor name from MAC address.

    Returns vendor name string, or 'Unknown' if not found.
    """
    prefix = _normalize_mac(mac)
    if not prefix:
        return "Unknown"

    db = _load_oui_db()

    if prefix in db:
        return db[prefix]

    return "Unknown"
